r = float(input("Input Radius : "))
s = int(input("Input side: "))
l = int(input("Input length: "))
b = int(input("Input breadth: "))
areac = 3.14 * r * r
perimeterc = 2 * 3.14 * r
areas = s * s
perimeters = 4 * s
arear = l * b
perimeterr = 2 * (l + b)
print("Area of Circle: ", areac)
print("Perimeter of Circle: ", perimeterc)
print("Area of Square: ", areas)
print("Perimeter of Square: ", perimeters)
print("Area of Rectangle: ", arear)
print("Perimeter of Rectangle: ", perimeterr)
